using System;


/***********************************************************************************************//**

タイマー（SoundManager.DeltaTime依存版）.
	
***************************************************************************************************/
public class TimerWithSoundManager : Timer
{
	/************************************************************************************//**
	コンストラクタ.
	****************************************************************************************/
	public TimerWithSoundManager (float interval, Action func = null) : base (interval, func)
	{
	}

	/************************************************************************************//**
	更新.
	
	@return なし		
	****************************************************************************************/
	public override void Update ()
	{
		base.t += SoundManager.Inst.DeltaTime;
		if (base.t > base.Interval)
		{
			base.ExecTick ();
			t = 0;
		}
	}
}


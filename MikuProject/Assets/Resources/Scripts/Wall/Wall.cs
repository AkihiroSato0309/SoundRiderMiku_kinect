using UnityEngine;
using System.Collections;


/***********************************************************************************************//**

壁クラス.
	
***************************************************************************************************/
public class Wall : MonoBehaviour
{
	/************************************************************************************//**
	衝突開始.
		
	@param [in] col 衝突情報
	
	@return なし		
	****************************************************************************************/
	void OnCollisionEnter (Collision col)
	{
		// 弾が当たったらSEを鳴らす
		if (col.gameObject.tag == "Bullet")
		{
			SoundManager.Inst.PlaySE (SE.HitWall);
		}
	}
}
